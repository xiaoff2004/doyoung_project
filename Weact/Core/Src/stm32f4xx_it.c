void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance==htim_IT_10.Instance)
	{
		EncoderA.cnt=(short)__HAL_TIM_GetCounter(&htim_EncoderA);
		__HAL_TIM_SET_COUNTER(&htim_EncoderA,0);
		EncoderB.cnt=(short)__HAL_TIM_GetCounter(&htim_EncoderB);
		__HAL_TIM_SET_COUNTER(&htim_EncoderB,0);
		
		EncoderA.SpeedOut=(float)EncoderA.cnt*100/(20*13*4);
		EncoderB.SpeedOut=(float)EncoderB.cnt*100/(20*13*4);
		speed_low_Filter(EncoderA.SpeedOut,SpeedBuffA);
		speed_low_Filter(EncoderB.SpeedOut,SpeedBuffB);
		PID21_Contrl(&PidALocation,&PidASpeed,&EncoderA);
		PID21_Contrl(&PidBLocation,&PidBSpeed,&EncoderB);	

		Motor_Setspeed(&EncoderA,PidASpeed.Output);
		Motor_Setspeed(&EncoderB,PidBSpeed.Output);

//		printf("%f,%f,%f,%f,%f,%f\r\n",EncoderB.SpeedIn,EncoderB.SpeedOut,PidBSpeed.Output,PidBSpeed.kp,PidBSpeed.ki,PidBSpeed.kd);
	}
}


